package inheritance;

public class Address {
	String house_Name,city,state,country;
	int house_No;
	public Address(String house_name, String city, String state, String country, int house_no) {
		super();
		this.house_Name = house_name;
		this.city = city;
		this.state = state;
		this.country = country;
		this.house_No = house_no;
	}
	

}
